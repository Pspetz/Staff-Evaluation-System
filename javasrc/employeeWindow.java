    private void usrnameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_usrnameActionPerformed
     try{
        connect();
        DefaultListModel m=new DefaultListModel();
        PreparedStatement st=connection.prepareStatement("SELECT am,afm,employeeexp_years,bio,sistatikes,certificate,awards,password FROM employee INNER JOIN user ON employee.username=user.username WHERE employee.username LIKE ?");
        PreparedStatement st1=connection.prepareStatement("SELECT * from job inner join requestevaluation ON job.id=requestevaluation.job_id WHERE requestevaluation.empl_username LIKE ?");
        st1.setString(1,this.usrname.getText());
        st.setString(1, this.usrname.getText());
         ResultSet res=st.executeQuery();
         ResultSet res1=st1.executeQuery();
         while(res1.next()){
             String count=res1.getString("position");
             m.addElement(count);
         }
         jList1.setModel(m);
         if(res.next()){
             this.AM.setText(res.getString(1));
                this.AFM.setText(res.getString(2));
                this.EXP.setText(res.getString(3));
                this.BIO.setText(res.getString(4));
                this.SISTA.setText(res.getString(5));
                this.CERT.setText(res.getString(6));
                this.AW.setText(res.getString(7));
                this.pass.setText(res.getString(8));
         }
         else{
             JOptionPane.showMessageDialog(rootPane, "check your credentials");
         }
         res.close();
            st.close();
     }
     catch(SQLException ex){
        
    }
    }//GEN-LAST:event_usrnameActionPerformed

