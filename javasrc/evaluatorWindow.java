    private void usrActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_usrActionPerformed
        try{
           connect();
           PreparedStatement st=connection.prepareStatement("SELECT exp_years,firm,idaks FROM evaluator WHERE username LIKE ?");
           st.setString(1, this.usr.getText());
           ResultSet res=st.executeQuery();
           
           if(res.next()){
               this.exp.setText(res.getString(1));
                this.firm.setText(res.getString(2));
                this.idaks.setText(res.getString(3));
                
           }
           else{
                 JOptionPane.showMessageDialog(rootPane, "check your credentials");
           }
        }
        catch(SQLException ex){
            
        }
    }//GEN-LAST:event_usrActionPerformed

