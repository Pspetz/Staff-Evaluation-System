     public static boolean ManagerFinder(String username,String password) {
        PreparedStatement user_statement = null;
        boolean userfound = false;
        try {
            
            
             user_statement=connection.prepareStatement("SELECT manager.managerUsername,password FROM user INNER JOIN manager ON manager.managerUsername=user.username WHERE user.username LIKE ? AND user.password LIKE ?");
             user_statement.setString(1,username);
             user_statement.setString(2,password);
             ResultSet results = user_statement.executeQuery();
            while (results.next()) {
                userfound = true;
            }
            results.close();
            user_statement.close();
        } catch (Exception ex) {
            userfound = false;
            ex.printStackTrace();
        } finally {
            try {
                if (user_statement == null) {
                    user_statement.close();
                }
            } catch (Exception ex2) {
            }//We want to do nothing
        }
        return userfound;
    }
     
